def get_demo_response(query: str, language: str = "en") -> str | None:
    """Check if query matches any demo topic. Returns pre-cached response or None."""
    query_lower = query.lower().strip()

    # Primary English keywords
    english_keywords = {
        "hi": ["hi", "hello", "hey", "good morning", "good evening"],
        "help": ["help", "what can you do", "how to use", "guide me"],
        "thank": ["thank", "thanks", "dhanyavad", "shukriya"],
        "fssai": ["fssai", "food license", "food safety"],
        "digital kit": ["digital kit", "generate kit", "full digital", "digital setup"],
        "upi": ["upi", "qr code", "payment setup", "phonepe", "paytm", "google pay", "bhim", "gpay"],
        "payment": ["payment", "digital payment", "accept payment", "online payment", "cashless"],
        "online_listing": ["list", "online", "google my business", "listing", "business online"],
        "government_scheme": ["scheme", "government", "govt", "pm svanidhi", "yojana", "pradhan mantri"],
        "loan": ["loan", "credit", "mudra", "borrow", "finance", "interest free"],
        "seo": ["seo", "search engine", "google ranking", "google maps", "search optimization"],
        "food_vendor": ["food", "restaurant", "cooking", "food stall", "hotel"],
        "training": ["training", "course", "skill", "learn", "education"],
        "license": ["license", "licence", "permit", "registration", "document"],
        "health": ["health", "medical", "hospital", "doctor", "insurance"],
        "emergency": ["emergency", "urgent", "help me", "helpline", "contact number"],
        "about": ["about", "what is this", "who made", "project", "internship"],
        "general": ["what", "how", "tell me", "explain", "info"],
    }

    # Check English keywords first
    for topic, words in english_keywords.items():
        if any(w in query_lower for w in words):
            response = DEMO_RESPONSES.get(topic, {}).get(language) or DEMO_RESPONSES.get(topic, {}).get("en")
            if response:
                return response

    # Check regional language keywords
    if language in REGIONAL_KEYWORDS:
        for topic, words in REGIONAL_KEYWORDS[language].items():
            if any(w in query_lower for w in words):
                response = DEMO_RESPONSES.get(topic, {}).get(language) or DEMO_RESPONSES.get(topic, {}).get("en")
                if response:
                    return response

    # Fallback: return general response in requested language
    fallback = DEMO_RESPONSES.get("general", {}).get(language) or DEMO_RESPONSES.get("general", {}).get("en")
    if fallback:
        return fallback

    return None
